package model.data.menu;

/**
 * Dữ liệu tên của menu admin
 */
public class DataNameMenuAdmin {
	private final String home = "Home page";
	private final String movies = "Movies";
	private final String showTime = "Showtimes";
	private final String history = "History";
	private final String revenue = "Revenue";
	private final String setting = "Setting";
	private final String logout = "Log out";

	/**
	 * @return the home
	 */
	public String getHome() {
		return home;
	}

	/**
	 * @return the movies
	 */
	public String getMovies() {
		return movies;
	}

	/**
	 * @return the showTime
	 */
	public String getShowTime() {
		return showTime;
	}

	/**
	 * @return the history
	 */
	public String getHistory() {
		return history;
	}

	/**
	 * @return the revenue
	 */
	public String getRevenue() {
		return revenue;
	}

	/**
	 * @return the setting
	 */
	public String getSetting() {
		return setting;
	}

	/**
	 * @return the logout
	 */
	public String getLogout() {
		return logout;
	}

}
